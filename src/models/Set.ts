/**
 * Data model for a Set. This is used as the view model. There's not persisted model.
 */
export class Set {
	
	/**
	 * The number of reps that were completed for this set.
	 */
	repCount: number;

	/**
	 * The weight completed for this set.
	 */
	weight: number;

	/**
	 * The weight completed for this set.
	 */
	time: string;

}
